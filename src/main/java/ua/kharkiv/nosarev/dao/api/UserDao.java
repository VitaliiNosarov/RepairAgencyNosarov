package ua.kharkiv.nosarev.dao.api;

import ua.kharkiv.nosarev.entitie.User;
import ua.kharkiv.nosarev.entitie.enumeration.UserRole;

import java.util.List;

public interface UserDao {
    /**
     * Return User from database which has equal email as userEmail
     *
     * @param userEmail not null String
     * @return User from Database with equal email
     */
    User getUserByEmail(String userEmail);

    /**
     * @param userId should be>0
     * @return User from Database with equal Id
     */
    User getUserById(long userId);

    /**
     * Returns List<Users> from database between startPosition and startPosition+recordsPerPage.
     *
     * @param startPosition  should be>0
     * @param recordsPerPage should be>0
     * @return
     */
    List<User> findUsers(long startPosition, long recordsPerPage);

    /**
     * Returns all users whose role is equal parameter role
     *
     * @param role
     * @return
     */
    List<User> getAllUsersByRole(UserRole role);

    /**
     * Save user to Database and returns same User with generated by Database id
     *
     * @param user notNul. should has correct fields. User id and balance can be 0
     * @return same User with Id == database user.id
     */
    User saveUser(User user);

    /**
     * Return amount of all users in Database which is need in pagination
     *
     * @return number of Users in Database
     */
    long amountOfUsers();
}
